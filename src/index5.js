//1. 삼항연산자
//condition ? exprIfTrue : exprIfFalse

const data = [1,2];

if(data,length === 0) {
  console.log("빈 배열입니다.")
} else {
  console.log("배열이 차있습니다.")
}
//삼항연산자를 써서 위에 if문을 한줄로 표현
data.length === 0 ? console.log("빈 배열"):console.log("배열이 차있음") //true : false

